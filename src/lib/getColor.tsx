export default function getColor(slug: string) {
	switch(slug) {
		case'melodic-intervals-1' : {
			return '#97A1BC';
		}
		case'melodic-intervals-2' : {
			return '#97A1BC';
		}
		case'synchronisation-1' : {
			return '#BB95B2';
		}
		case'synchronisation-2' : {
			return '#BB95B2';
		}
		case 'rhythm-1' : {
			return '#eda29e';
		}
		default : {
			return '#97A1BC'
		}
	}
}